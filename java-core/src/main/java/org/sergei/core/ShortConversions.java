package org.sergei.core;

/**
 * @author Sergei Visotsky
 */
public class ShortConversions {

    public static void main(String[] args) {

        short s = 'A';
        char a = 'A';
        System.out.printf("Short value: %s\n", s);
        System.out.printf("Char value: %s\n", a);
        System.out.printf("Char value as integer: %s\n", (int) a);

    }

}
